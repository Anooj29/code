# Basic arithmetic operations
a <- 10  # Assign 10 to variable 'a'
b <- 5   # Assign 5 to variable 'b'

# Perform operations
sum <- a + b
difference <- a - b
product <- a * b
quotient <- a / b

# Print results
print(paste("Sum:", sum))
print(paste("Difference:", difference))
print(paste("Product:", product))
print(paste("Quotient:", quotient))
